// npm: This is a package manager in java script like pip (for python).

/*
Installing/Uninstalling packages using npm:

We can install javascript packages using "npm install package_name" or "npm i package_name"
We can uninstall javascript packages using "npm uninstall package_name"
*/

/* 
use 'npm install' to install all dependencies of your project in  'node module' folder.

Note: All dependencies related to your project are available in node module directory.
*/

/*
installing a specific version using npm:

npm install module_name@version
ex.- npm install slugify@1.3.5

In version 1.3.5 (1 denotes major changes, 3 denotes minor changes and 5 denotes bug fixes).
example: 1.0.0 (new version)
1.0.1 (bug fixes)
1.1.0 (adding some new features)
2.0.0 (major changes. adding/removing some functions/features).
*/

/*
GLOBALLY INSTALL A PACKAGE:
npm install package_name --global
i.e. npm install nodemon --global
*/