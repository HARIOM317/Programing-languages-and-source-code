let a = 10;
let b = 20;

// Arithmetic Operators
console.log("\nArithmetic Operators");
console.log("a+b = ", a+b);
console.log("a-b = ", a-b);
console.log("a*b = ", a*b);
console.log("a/b = ", a/b);
console.log("a%b = ", a%b);
console.log("++a = ", ++a);
console.log("--b = ", --b);

// Assignment Operators
console.log("\nAssignment Operators");
let x = 5;
x += 10; // x = 5 + 10 = 15
x -= 5; // x = 15 - 5 = 10
x *= 10; // x = 10 * 10 = 100
x /= 5; // x = 100 / 5 = 20
x **= 2; // x = 20 ^ 2 = 400
x %= 5; // x = 400 % 5 = 0
console.log("x = ", x);

// Comparison Operator
console.log("\nComparison Operators");
let a1 = 10;
let b1 = "10";
console.log("a1 == b1: ", a1 == b1);
console.log("a1 === b1: ", a1 === b1);
console.log("a1 !== b1: ", a1 !== b1);
console.log("a1 > b1: ", a1 > b1);
console.log("a1 < b1: ", a1 < b1);
console.log("a1 <= b1: ", a1 <= b1);
console.log("a1 >= b1: ", a1 >= b1);
console.log("a1 != b1: ", a1 != b1);

a < b ? console.log("b is greater") : console.log("a is greater");

// Logical Operator
console.log("\nLogical Operators");
let p = 10;
let q = 20;
let p1 = true;
let p2 = false;
let p3 = true;

console.log("p1 && p2 = ", p1 && p2);
console.log("p1 && p3 = ", p1 && p3);
console.log("p1 || p2 = ", p1 || p2);
console.log("!p1 = ", !p1);
console.log("p < q && p == 10: ", p < q && p == 10);





