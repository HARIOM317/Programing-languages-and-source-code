// there are 6 falsy values in js
// 0, "", undefined, null, NaN and false
// except these falsy values everything will be true in if statement

// 0
if(0){
    console.log("0 IS NOT a FALSY VALUE in js.\n");
} else {
    console.log("0 IS a FALSY VALUE in js.\n");
}

// ""
if ("") {
    console.log("'' IS NOT a FALSY VALUE in js.\n");
} else {
    console.log("'' IS a FALSY VALUE in js.\n");
}

// undefined
if (undefined) {
    console.log("undefined IS NOT a FALSY VALUE in js.\n");
} else {
    console.log("undefined IS a FALSY VALUE in js.\n");
}

// null
if (null) {
    console.log("null IS NOT a FALSY VALUE in js.\n");
} else {
    console.log("null IS a FALSY VALUE in js.\n");
}

// NaN
if (NaN) {
    console.log("NaN IS NOT a FALSY VALUE in js.\n");
} else {
    console.log("NaN IS a FALSY VALUE in js.\n");
}

// false
if (false) {
    console.log("false IS NOT a FALSY VALUE in js.\n");
} else {
    console.log("false IS a FALSY VALUE in js.\n");
}