console.log(Math.PI);   // return value of PI
console.log(Math.pow(2, 3));    // power of number
console.log(Math.sqrt(47));     // square root of number
console.log(Math.abs(-987.55));     // return positive value
console.log(Math.round(10.598));    // round number by decreasing or increasing the number by 1
console.log(Math.ceil(4.01));   // ceil value that is 5
console.log(Math.floor(4.99));  // floor value that is 4
console.log(Math.min(3, 65, 9, 2));     // min number
console.log(Math.max(3, 65, 9, 2));     // max number
console.log(Math.random()*100);     // random number b/w 0 - 100
console.log(Math.trunc(87.9878));   // return integer part of a number