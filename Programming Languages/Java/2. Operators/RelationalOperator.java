public class RelationalOperator {
    public static void main(String[] args) {
        int a = 8;
        int b = 5;
        System.out.printf("a = %d and b = %d\n\n", a, b);
        // Comparison operators (==, >, <, <=, >=, !=)
        System.out.println("a==b : "+ (a==b));
        System.out.println("a>b : "+ (a>b));
        System.out.println("a<b : "+ (a<b));
        System.out.println("a>=b : "+ (a>=b));
        System.out.println("a<=b : "+ (a<=b));
        System.out.println("a!=b : "+ (a!=b));
    }
}
