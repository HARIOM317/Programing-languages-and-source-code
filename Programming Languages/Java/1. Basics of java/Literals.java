public class Literals {
    public static void main(String[] args) {
        byte age = 28;      // integer literal
        char ch = 'A';      // character literal
        float f1 = 15.6f;     // float literal
        // Note: We need to tell in float data type that f1 is a float using f in last of the number.
        double d1 = 2555.16;     // double literal
        long length = 65798798L;        // long literal
        short height = 63;      // short literal
        boolean bool = true;        // boolean literal
        String str = "Hariom Rajput";       // string literal

        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(length);
        System.out.println(height);
        System.out.println(bool);
        System.out.println(str);
        // println is used for adding new line after print statement.
    }
}
