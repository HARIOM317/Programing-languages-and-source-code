a = 25
b = 5
print("a = ", a)
print("b = ", b)
a += b  # a = a+b
print("a + b = ", a)
a -= b  # a = a-b
print("a - b = ", a)
a *= b  # a = a*b
print("a * b = ", a)
a /= b  # a = a/b
print("a / b = ", a)
a //= 3     # a = a//3
print("a // 3 = ", a)
a **= 2     # a = a ** 2
print("a ^ 2 = ", a)
a %= 7  # a = a%7
print("a % 7 = ", a)
